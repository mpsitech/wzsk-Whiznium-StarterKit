/******************************************************************************
 class QryWzskObjList::VecVOrd
 ******************************************************************************/

uint QryWzskObjList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "ogr") return OGR;
	if (s == "tit") return TIT;

	return(0);
};

string QryWzskObjList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == OGR) return("ogr");
	if (ix == TIT) return("tit");

	return("");
};

void QryWzskObjList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i = 1; i <= 2; i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

